public class Loops {
    public static void main(String args[]) {

// for-loop
        for(int i=0;i<3;i++) {
            System.out.println("Hello World");
        }
        System.out.println("\n");

// while loop
        int j = 0;
        while(j<=20) {
            System.out.println(j);
            j++;
        }
        System.out.println("\n");

// do-while loop
        int k = 0;
        do {
            System.out.println(k);
            k++;
        } while (k<=20);
        System.out.println("\n");

    }
}